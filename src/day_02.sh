#!/bin/bash

#----------------------Verificação de entrada---------------------------------#
if test -z "$1"
then
	echo 'erro'
	exit 1
fi
#----------------------Constantes---------------------------------------------#
pontuacao=0
#----------------------Constantes---------------------------------------------#
declare -A resultado=( 
	["A X"]="4" 
	["A Y"]="8" 
	["A Z"]="3" 
	["B X"]="1" 
	["B Y"]="5" 
	["B Z"]="9" 
	["C X"]="7" 
	["C Y"]="2" 
	["C Z"]="6")
#----------------------Codigo-------------------------------------------------#
while read linha 

do
	pontuacao=$(( $resultado + ${resultado["$linha"]} ))
done < "$1"

echo $pontuacao

#-----------------------------------------------------------------------------#

