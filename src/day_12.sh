#!/bin/bash

# day_xx.sh

#-----------------------------------Test input--------------------------------#
test -z "$1" && echo "Erro!"  && exit 1 
#-----------------------------------Variable----------------------------------#
declare -A map
declare -i col=0
declare -i lin=0
declare -- start=""
declare -- finish=""
#-----------------------------------Functions---------------------------------#
#-----------------------------------MAP Parser--------------------------------#
while read -N 1 char
do
    if [[ "$char" =~ [a-zA-Z] ]] 
    then
        [ "$char" = "S" ] && start="$lin $col"
        [ "$char" = "E" ] && finish="$lin $col"
        map[$lin $col]=$( printf "%d\n" "'$char" )
        col=col+1 
    else
        lin=lin+1
        col=0 
        continue
    fi
done < $1
#--------------------------------------|--------------------------------------#
declare -p map start finish
